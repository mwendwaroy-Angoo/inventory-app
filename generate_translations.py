def collect_messages():
    """Return translatable strings from the seed list plus tagged templates and Python files."""
    messages = list(MESSAGES)
    seen = set(messages)

    templates_dir = os.path.join(BASE_DIR, 'templates')
    for root, dirnames, filenames in os.walk(templates_dir):
        dirnames.sort()
        for filename in sorted(filenames):
            if not filename.endswith('.html'):
                continue
            path = os.path.join(root, filename)
            with open(path, 'r', encoding='utf-8') as f:
                content = f.read()
            for message in TEMPLATE_TRANS_PATTERN.findall(content):
                if message not in seen:
                    messages.append(message)
                    seen.add(message)

    python_paths = [
        os.path.join(BASE_DIR, 'accounts', 'forms.py'),
        os.path.join(BASE_DIR, 'accounts', 'models.py'),
        os.path.join(BASE_DIR, 'accounts', 'views.py'),
        os.path.join(BASE_DIR, 'core', 'forms.py'),
        os.path.join(BASE_DIR, 'core', 'models.py'),
        os.path.join(BASE_DIR, 'core', 'views.py'),
    ]
    for path in python_paths:
        with open(path, 'r', encoding='utf-8') as f:
            content = f.read()
        for message in PYTHON_GETTEXT_PATTERN.findall(content):
            if message not in seen:
                messages.append(message)
                seen.add(message)

    return messages


def resolve_translation(lang_code, translations, message):
    """Return the best available translation for a message."""
    if message in translations:
        return translations[message]

    if FALLBACK_LANGUAGE:
        fallback_translations = TRANSLATIONS.get(FALLBACK_LANGUAGE, {})
        if message in fallback_translations:
            return fallback_translations[message]

    # Final fallback keeps unfinished languages readable without forcing a different local language.
    return message

